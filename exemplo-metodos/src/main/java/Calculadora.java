import java.util.List;

public class Calculadora {
    Double somar(Double num01, Double num02){
        return num01 + num02;
    }

    Double somar(Double num01, Double num02, Double num03){
        return num01 + num02 + num03;
    }

    Double subtrair(Double num01, Double num02){
        return num01 - num02;
    }

    Double multiplicar(Double num01, Double num02){
        return num01 * num02;
    }
    Double dividir(Double num01, Double num02){
        return num01 / num02;
    }
}

